/*Смена селектов и стилей при нажатии radio button */

$(document).ready(function() {

  $.each(animals.cats, function (index, item) {
    $('#breed').append($("<option value=\"" + item.breed + "\">" + item.breed + "</option>"));
  });

  $('.radio').on('change', radioChange);

  function radioChange() {
    if($("#radioCat").is(":checked")){
      console.log("cat");
      $('.result.cat').addClass("active");
      $('.article.cat').addClass("active");
      $('.info.cat').addClass("active");
      $('.result.dog').removeClass("active");
      $('.article.dog').removeClass("active");
      $('.info.dog').removeClass("active");

      $('#breed').empty();
      $.each(animals.cats, function (index, item) {
        $('#breed').append($("<option value=\"" + item.breed + "\">" + item.breed + "</option>"));
      });
    } if ($("#radioDog").is(":checked")){
      console.log("dog");
      $('.result.dog').addClass("active");
      $('.article.dog').addClass("active");
      $('.info.dog').addClass("active");
      $('.result.cat').removeClass("active");
      $('.article.cat').removeClass("active");
      $('.info.cat').removeClass("active");

      $('#breed').empty();
      $.each(animals.dogs, function (index, item) {
        $('#breed').append($("<option value=\"" + item.breed + "\">" + item.breed + "</option>"));
      });
    }
  }
});

